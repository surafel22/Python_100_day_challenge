print("Welcome to remaining life calculator")
age = float(input("Enter your age in years :"))
r_age_years = 90 - age
r_age_months = r_age_years * 12
r_age_weeks = r_age_years * 52
r_age_days = r_age_weeks * 7
print(f"You left {r_age_years}years ,{r_age_months} months, {r_age_weeks} weeks , {r_age_days} days ")
